
def area(a, h):
    """
    Принимает сторону треугольника a и высоту h, опирающуюся на сторону a,
    возвращает площадь треугольника
    """
    return a * h / 2


def perimeter(a, b, c):
    """Принимает стороны треугольника a,b,c, возвращает его периметр"""
    return a + b + c



